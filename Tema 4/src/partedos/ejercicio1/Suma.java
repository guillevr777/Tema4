package partedos.ejercicio1;

public class Suma {
		
		public int Suma (int numeroUno, int numeroDos) {
			int suma = 0;
			
			suma += numeroUno;
			suma += numeroDos;
			
			return suma;
		}
		public double Suma (double numeroUnoDouble, double numeroDosDouble) {
			int suma = 0;
			
			suma += numeroUnoDouble;
			suma += numeroDosDouble;
			
			return suma;
		}
}
